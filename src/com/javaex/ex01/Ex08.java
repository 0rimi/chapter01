package com.javaex.ex01;

public class Ex08 {
	
	public static void main(String[] args) {
		
		//관계연산자
		//=는 대입입니다! a=3은 3을 a에 대입~
		int n1 = 5;
		int n2 = 3;
		System.out.println(n1<n2); //false
		System.out.println(n1>n2); //true
		System.out.println(n1<=n2); //false
		System.out.println(n1>=n2);//true
		System.out.println(n1==n2);//false
		System.out.println(n1!=n2);//true
		
		
	}

}
